<h2 class="hcls">Call for Tutorials</h2>

<p>
We seek to extend the SIGCOMM experience by tutorials on selected
topics given by renowned scientists and practitioners in their fields.
We therefore solicit proposals for full-day or half-day tutorials on
topics relevant to the SIGCOMM community.
</p>

<p>
Tutorials must cover advanced topics that fit the scope of SIGCOMM and
are of current interest to the SIGCOMM community.  Tutorials may be
lectures, interactive workshops, hands-on training, or any combination
of the above. Exploring diverse ways of interacting with the audience
is welcome as are cross-disciplinary topics.
</p>

<p>
Tutorial proposals should be submitted in PDF format, not exceeding
three (3) pages in total, and be sent to the SIGCOMM 2012 
<a href="mailto:sigcomm2012-gen-chairs@acm.org">general chairs</a>
</p>

<p>
Proposals must include:
</p>

<ul>

<li>a motivation for the tutorial (why this, why now, why at SIGCOMM?)</li>

<li>an outline of the tutorial content</li>

<li>the type of tutorial (e.g., lecture vs. hands-on)</li>

<li>references to previous iterations of the tutorial (if applicable)
  including their date, venue, topics and number of participants
  and the motivation for the new proposal</li>

<li>requirements for the tutorial room (please note that our
  capabilities in fulfilling unusual requests are limited)</li>

<li>requirements for the attendants (e.g., must bring own laptop or other
  hardware, familiarity with certain technologies or topics, etc.)</li>

<li>any limitations (e.g., number of participants)</li>

</ul>

