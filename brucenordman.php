<b> Title: Local Power Distribution with Nanogrids </b> 

<p style="text-align:justify"> <b> Abstract: </b> Many aspects of our electricity systems are rapidly changing, including the ability to locally generatepower with renewable energy sources and integrate local storage. Key factors that led to our current grid architecture will no longer hold in the near future. We urgently need to reconsider how we shoulddistribute power within buildings. This presentation will describe a new architecture for a network modelof electricity distribution within buildings. In this model, individual devices are organized into nanogrids(a single domain of power), with nanogrids networked to each other, to local generation, and to abuilding- wide microgrid. Nanogrids inherently work well with DC power for efficiency and other benefits,though should also be extended to AC systems as feasible. This new model for electricity distribution inbuildings is implemented with a layered model of power â€“ that isolates communication about power fromcommunication for control, function, and all other purposes.
</p>

