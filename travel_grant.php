<h2 class="hcls">Where and How to Apply</h2>

<p>
All application materials (applicant's info, CV and and letters) should be submitted electronically to the following website <a class="lnkcls" href="http://tiny.cc/eEnergy15TG" >http://tiny.cc/eEnergy15TG</a>.
</p>

<p>
The applicant needs to prepare:
</p>

<ol>
<li>
an up-to-date CV
</li>
<li>
an application letter, and
</li>
<li>
a letter from their supervisor (Ph.D. adviser for students, Ph.D or postdoc adviser for postdocs).
</li>
</ol>

<p>
The applicant's letter should be clearly divided in the following sections:
</p>

<ol>

<li>
an explicit identification of (a) the applicant's current country of study or work, (b) academic status (student, postdoc), (c) graduation or completion date.
</li>

<li>
<em>the title of the paper that has been accepted to the e-energy conference/workshop;</em>
</li>

<li>
a brief summary of where the applicant is studying/working (provide a URL for a home page if one exists) and who the applicant's advisor/supervisor is; any additional information the applicant feels is relevant to support their case;
</li>

<li>
a brief summary of research interests and plans for future research; and
</li>

<li>
why the conference attendance is important to the applicant's research
</li>
<li>
a cost estimate of the trip, listing the cost of airfare, hotel, and the source of any price quote.
</li>
</ol>

<p>
The supervisor's letter should be clearly divided in the following sections:
</p>

<ol>
<li>
the applicant's good standing in the institution;
</li>
<li>
explain why the applicant would benefit from attending e-Energy as well as how he/she could contribute to the conference; and
</li>
<li>
explain the current funding status and why the applicant is in need of the travel grant.
</li>
</ol>

<p>
Supervisor letters will be requested by email. Applicant is requested to provide their referee's current email address.
</p>

