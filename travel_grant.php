<h2 class="hcls">ACM e-Energy 2015 Travel Grants</h2>

<p>
The Conference Organizing Committee would like to provide as much travel support to as many conference attendees as possible. Support is provided for a variety of different types of attendees, from graduate students to post-doctoral scholars. The amount of support depends on the type of attendee and available funding. Attendees from around the world are encouraged to apply. Support for e-Energy 2015 travel grants will be available to students, and post-docs, no matter which university you are working at.
</p>

<p>
Please consider the following important information before applying:
</p>
<ol>
<li>
The application process is the same for all types of attendees, and is described below.
</li>
<li>
Travel grant awards are meant to partially cover the cost of attending and participating in e-Energy 2015. 
</li>
<li>
Flat grants of $1000 and $500 will be offered to international (whose home institution is outside India) and local attendees respectively.
</li>
<li>
Award recipients would need to pay for expenses ahead of time and get reimbursed after the conference by way of the award.
</li>
<li>
The exact number of awards will depend on the availability of funds and will be determined as funding amounts are finalized.
</li>
</ol>


<h2 class="hcls">Selection Criteria</h2>

<p>The following criteria will be used in the selection process:</p>

<ol>
<li>
First preference will be given to applicants who have a regular paper accepted at the conference. Only one author per paper will be given this preference. 
</li>
<li>
Second preference will be given to applicants with poster/demo/challenge/workshop papers. Only one author per paper will be given this preference. 
</li>
<li>
Third preference will be given to researchers who do not have any accepted submissions. However, they must have demonstrated serious interest in the energy domain through coursework and/or project experience. 
</li>
<li>
Advanced students close to graduation or in the job market will also be given priority, when possible.
</li>
</ol>

<h2 class="hcls">Eligibility</h2>
<ol>
<li>
The applicants must be full-time graduate students or post-docs studying or working at established universities. 
</li>
<li>
The applicants must register paying the student-registration fee  and attend the e-Energy main conference to get the grant.
</li>
<li>
Applicants should have serious interest in energy research and demonstrate his/her potential of being an active researcher in the field. This will be demonstrated by research projects, publications, and recommendation letters from their advisors.
</li>
</ol>

<h2 class="hcls">Award</h2>
On the last day of the conference, the travel award winners will receive the grant checks. In addition, the travel award winners will receive Certificates of Recognition presented at the e-Energy 2015 banquet dinner.
<ol>
<li>
International award recipients (whose home institution is outside India) will $1000.
</li>
<li>
Domestic award recipients (whose home institution is within India) will receive $500.
</li>
</ol>


