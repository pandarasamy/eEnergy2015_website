<h2 class="hcls">Local Information</h2>
<p style="text-align:justify">
Bangalore, officially known as Bengaluru, is the third largest city in India and is the center of India's fifth-largest metropolitan area. 
Located in southern India on the Deccan Plateau, it is the capital of the southern Indian state of Karnataka. 
Bangalore is known as the "Silicon Valley of India" because of its role as the nation's leading information technology (IT) exporter. 
Located at a height of over 3,000 feet (914.4 m) above sea level, Bangalore is known for its pleasant climate throughout the year. 
Its elevation is the highest among the major large cities of India. </p>

