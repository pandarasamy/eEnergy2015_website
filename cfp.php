<h2 class="hcls">Call for Papers</h2>

<p style="text-align:justify">
Computing and communication technologies impact energy systems in two distinct ways. The exponential growth in deployment of these technologies has made them large-scale energy consumers. Therefore, new architectures, technologies and systems are being developed and deployed to make computing and networked system more energy efficient. Additionally, and perhaps more importantly, these technologies are at the center of the on-going revolution in next generation 'smart' and sustainable energy systems. They measure, monitor and control energy systems such as the smart grid; inform and shape human demand; aid in the prediction, deployment, storage and control of energy resources; and determine how utilities, generators, regulators, and consumers measure, analyze, and collectively control system elements.
</p>
<p style="text-align:justify">
The sixth International Conference on Future Energy Systems (ACM e-Energy), to be held in Bangalore, India in July 2015 aims to be the premier venue for researchers working in the broad areas of computing and communication for smart energy systems (including the smart grid), and in energy-efficient computing and communication systems. By bringing together researchers in a high-quality single-track conference with significant opportunities for individual and small group interactions, it will serve as a major forum for presentations and discussions that will shape the future of this area.
</p>
<p style="text-align:justify">
We solicit high-quality papers in the area of computing and communication for the Smart Grid and energy-efficient computing and communications. We welcome submissions describing theoretical advances as well as system design, implementation and experimentation. ACM eEnergy is committed to a fair, timely, and thorough review process providing authors of submitted papers with sound and detailed feedback.
</p>

<p style="text-align:justify">Relevant topics for the conference include, but are not limited to the following:: </p>

<ul>
<li>Advances in monitoring and control of smart homes and buildings </li>
<li>Sensing, monitoring, control, and management of energy systems </li>
<li>Energy-efficient computing and communication, including energy-efficient data centers </li>
<li>The impact of storage integration on the smart grid </li>
<li>Electric Vehicle monitoring and control </li>
<li>Distribution and transmission network control techniques </li>
<li>Microgrid and distributed generation management and control </li>
<li>Modeling, control, and architectures for renewable energy generation resources </li>
<li>Smart grid communication architectures and protocols </li>
<li>Privacy and security of smart grid infrastructure </li>
<li>Innovative pricing and incentives for demand-side management</li>
<li>Novel technologies to enhance reliability and robustness of energy systems </li>
<li>HCI for energy monitoring, management, and awareness </li>
<li>User studies and behavioral change enabled by computing and communication technologies </li>
<li>Data analytics for the smart grid and energy-efficient systems </li>
<li>Modeling, management and control of variability and uncertainty in energy supply and demand</li>

</ul>

<h2 class="hcls">Submissions</h2>

<p style="text-align:justify">Three types of contributions are solicited: </p>
<ul>
<li>
<p style="text-align:justify">
<b>Regular papers</b>, up to 10 pages in ACM double-column format, should present original theoretical and/or experimental research in any of the areas listed above that has not been previously published, accepted for publication, or is not currently under review by another conference or journal. Regular papers are intended to describe high quality research work based on novel ideas that have been thoroughly evaluated. The 10 pages limit is intended to be an upper bound and we encourage the authors to take fewer pages if appropriate. The review process for regular papers will include a thorough evaluation by the program committee, opportunity for authors to respond to reviews, and a shepherding process to ensure compliance with review comments before the paper acceptance is finalized.
</li>
<li>
<b>Challenge Papers</b>, up to 6 pages in ACM double-column format, should present revolutionary new ideas that challenge existing assumptions prevalent among the e-Energy research community. Challenge papers should provide stimulating ideas or visions that may open up exciting avenues and/or influence the direction of future research. Descriptions of new products or evolution of existing work are not appropriate topics for papers in this category. While an exhaustive evaluation of the proposed ideas is not necessary, insight into and in-depth understanding of the issues is expected. Challenges papers will be reviewed by the program committee and will be part of the technical program and published in the proceedings. The title of these papers must start with the prefix 'Challenge:' i.e., 'Challenge: Rest of the Title.'
</li>
<li>
<b>Poster and Demo Abstracts</b>, up to 2 pages in ACM double-column format showcasing works-in-progress; accepted posters/demos will be presented at the conference. Topics of interest are the same as research topics listed above. Preference will be given to posters/demos where the primary contribution is from one or more students. Additionally, the demo abstracts should be accompanied by a third page (which will not be published) describing the script of the demo to be presented as well as any special resource requirements (space, networking, electricity etc.). The title for the abstracts must have the prefix 'Poster Abstract:' or 'Demo Abstract:' as appropriate.
</li>
</ul>
</p>
<p style="text-align:justify">The submission must be in PDF format with all fonts embedded, and be formatted according to the official ACM Proceedings format. Papers that do not meet the size and formatting requirements will not be reviewed. Word and LaTeX templates are available at <a href="http://www.acm.org/sigs/publications/proceedings-templates">http://www.acm.org/sigs/publications/proceedings-templates</a>. Please note that you must use the 'Strict Adherence to SIGS style' template option. Submissions are to be made via the Easychair website at: <a href="https://easychair.org/conferences/?conf=eenergy2015">https://easychair.org/conferences/?conf=eenergy2015</a></p>

